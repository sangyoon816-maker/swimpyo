export const getPlacesByEmotion = (emotion: Emotion) =>
  PLACES.filter((p) => p.emotions.includes(emotion));

export const getPlacesByCategory = (category: string) =>
  PLACES.filter((p) => p.category === category);

export const getPlacesByDistrict = (district: string) =>
  PLACES.filter((p) => p.district === district);

/**
 * "지역(area)" filter shown in the map UI groups by commercial/cultural
 * area rather than legal 동(neighborhood) — e.g. '망원' covers both
 * 망원동 and 성산동. This maps each area label to its member neighborhoods.
 */
export const AREAS = ['성수', '연남', '망원', '을지로', '잠실', '여의도', '한남', '성북'] as const;
export type Area = (typeof AREAS)[number];

export const AREA_NEIGHBORHOODS: Record<Area, string[]> = {
  성수: ['성수동', '성수동1가', '성수동2가', '응봉동'],
  연남: ['연남동'],
  망원: ['망원동', '성산동'],
  을지로: ['을지로'],
  잠실: ['잠실동', '신천동', '오금동', '방이동'],
  여의도: ['여의도동'],
  한남: ['한남동'],
  성북: ['성북동'],
};

export const getPlacesByArea = (area: Area) =>
  PLACES.filter((p) => AREA_NEIGHBORHOODS[area].includes(p.neighborhood));

/** Inverse of AREA_NEIGHBORHOODS — which area (if any) a given place belongs to. */
export const getAreaForPlace = (place: Place): Area | null =>
  AREAS.find((area) => AREA_NEIGHBORHOODS[area].includes(place.neighborhood)) ?? null;

export const getPlacesByTag = (tag: PlaceTag) =>
  PLACES.filter((p) => p.tags.includes(tag));

export const getPlaceById = (id: string) =>
  PLACES.find((p) => p.id === id);

/** Districts actually present in PLACES — feeds a future region filter UI. */
export const DISTRICTS = Array.from(new Set(PLACES.map((p) => p.district))).sort();
